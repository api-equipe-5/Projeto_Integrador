package modeloBeans;

/**
 *
 * @author Daniel
 */
public class BeansVeiculo {
    private Integer cod;
    private String cor;
    private String modelo;
    private String placa;
    private String ano;
 
    
    
    
    private String vpesquisa;

    public int getCod() {
        return cod;
    }

    public void setCod(int cod) {
        this.cod = cod;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getAno() {
        return ano;
    }

    public void setAno(String ano) {
        this.ano = ano;
    }

    public String getVpesquisa() {
        return vpesquisa;
    }

    public void setVpesquisa(String vpesquisa) {
        this.vpesquisa = vpesquisa;
    }


}